function createAudio(json) {
    $$("body").removeView('content');
    $$("body").addView({
        id: "content",
        css: 'boxLetter',
        rows: [
            {
                height: 150,
                cols: [
                    {
                        view: 'label',
                        label: json.addValue,
                        css: 'pLetter',
                        width: 300,
                        height: 60
                    },
                    {
                        id: "textNoticeCont",
                        hidden: json.value == "",
                        width: 300,
                        template: '<iframe width="100%" height="166" scrolling="no" frameborder="no" src="' + json.value + 'liking=false&amp;sharing=false&amp;show_artwork=false&amp;color=ff9900&amp;download=false&amp;auto_play=false&amp;hide_related=true&amp;show_comments=false&amp;show_user=false&amp;show_reposts=false"></iframe>'
                    },
                    {}
                ]
            }
        ]
    });
}

function getAllLetters(id) {
    get_ajax('/study/wr/lrn/getAllLetters', 'GET', {id: id}, function (gson) {
        createAudio(gson.message);
    });
}